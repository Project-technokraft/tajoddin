          <li>Stamp Duty<p>A Stamp duty is a kind of tax which is levied upon by the state government when an immovable property is bought and sold by any individual. A stamp duty is usually charged as per the provision of The Indian Stamps Act. The stamp duty is chargeable on the entire agreement value as mentioned in the Sales Agreement. The Stamp duty rate varies from State to State and it may also vary for different cities with any state.<br><i>For example :</i>As of today the Stamp duty rate  for buying a real estate property in the city of Mumbai is  5% and  6% in Thane and Navi Mumbai. If by any chance when the agreement value is less than the Ready Reckoner rates, stamp duty would be charged as per Ready Reckoner rates


</p>

</li>
          <li>Registration Charges<p>Registration charges would also be levied on a any real estate transaction along with Stamp duty charges. Registration charges are also levied as per the provisions of the Indian Registration Act. Registration charges are levied in order to get the transaction registered with the Registrar. In Mumbai registration charges can be either 1% of the agreement value or Rs. 30,000/- whichever is the lowest.

</p> </li>
          <li>TDS (Tax deducted at source):<p>In real estate transactions involving properties that are worth more than 50 Lakhs, a TDS worth of 1% of the agreement value needs to be paid to the Govt. of India.


</p></li>
